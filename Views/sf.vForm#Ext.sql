  sf.FormVersion zfvv on maxfv.FormSID = zfvv.FormSID and maxfv.VersionNo = zfvv.VersionNo and maxfv.VersionNo > 0 -- no row is found if no published version exists
left outer join
  sf.FormVersion zfvr on maxfv.FormSID = zfvr.FormSID and maxfv.RevisionNo = zfvr.RevisionNo
--! </MoreJoins>
