	sf.Form            form
join
	sf.ApplicationUser au     on form.ApplicationUserSID = au.ApplicationUserSID
join
	sf.FormType        ftype  on form.FormTypeSID = ftype.FormTypeSID
--! <MoreJoins>
left outer join
  (
   select
     x.FormSID
    ,max(x.VersionNo)   VersionNo
    ,max(x.RevisionNo)  RevisionNo
   from
    sf.FormVersion x
   group by
    x.FormSID
  ) maxfv on form.FormSID = maxfv.FormSID
