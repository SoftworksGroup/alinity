	dbo.StateProvince zsp on city.StateProvinceSID = zsp.StateProvinceSID
join
	dbo.Country				zctry on zsp.CountrySID = zctry.CountrySID
join
(
	select
		isnull(convert(bit, sf.fConfigParam#Value('IsInsuranceEnabled')), 1) IsInsuranceEnabled
)										zins on 1 = 1
outer apply
(
	select
		dateadd(month, isnull(convert(smallint, sf.fConfigParam#Value('OrgReviewMonths')), 12), convert(smalldatetime, o.LastVerifiedTime)) NextReviewTime
)										znr
outer apply
(
	select
		dateadd(day, 1, max(oon.ExpiryDate)) OrgNameEffectiveDate
	from
		dbo.OrgOtherName oon
	where
		oon.OrgSID = o.OrgSID
) zned;
--! </MoreJoins>
